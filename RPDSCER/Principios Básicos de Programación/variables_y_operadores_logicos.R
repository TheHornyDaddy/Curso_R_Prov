# Operadores lógicos:
# ==
# !=
# <
# >
# <=
# >=
# ! contrario
# | or
# &
# isTRUE(x)

# Ejemplos:
var1 <- 4 < 5
var1
typeof(var1)

var2 <- !TRUE
var2

var3 <- !(5 > 1)
var3

var4 <- TRUE | FALSE
var4

var5 <- FALSE | FALSE
var5

var6 <- var1 | var3
var6

var7 = var1 & var3
var7

var8 <- isTRUE(var1)
var8

var9 <- isTRUE(var3)
var9